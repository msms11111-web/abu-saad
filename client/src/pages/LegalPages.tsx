import { ReactNode } from 'react'

// Shared shell so all legal pages look consistent
function LegalShell({ title, updated, children }: { title: string; updated?: string; children: ReactNode }) {
  return (
    <div className="container-max py-12 max-w-4xl">
      <h1 className="text-3xl md:text-4xl font-bold mb-2">{title}</h1>
      {updated && <p className="text-sm text-gray-500 mb-8">آخر تحديث: {updated}</p>}
      <div className="card p-6 md:p-10 space-y-6 leading-relaxed text-gray-700">{children}</div>
    </div>
  )
}

function Section({ title, children }: { title: string; children: ReactNode }) {
  return (
    <section>
      <h2 className="text-xl font-bold text-secondary mb-3">{title}</h2>
      <div className="space-y-2">{children}</div>
    </section>
  )
}

